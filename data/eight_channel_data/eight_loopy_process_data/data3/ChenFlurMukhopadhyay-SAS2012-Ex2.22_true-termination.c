extern int __VERIFIER_nondet_int(void);



int main() {
    int x, y, oldy;
    x = __VERIFIER_nondet_int();
    y = __VERIFIER_nondet_int();
    while (x > 0 && y <= -oldy) {
        x = y;
        oldy = y;
        y = __VERIFIER_nondet_int();
    }
    return 0;
}